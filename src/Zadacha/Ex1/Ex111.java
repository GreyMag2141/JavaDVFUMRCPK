import java.util.Scanner;

/*Написать программу для определения диаметра окружности,
которую можно получить из отрезка проволоки длиной Х (X задает пользователь).
 */
public class Ex111 {
    public static void main(String[] args) {
        Scanner in= new Scanner(System.in);
        System.out.print("Введите длину отрезка: ");
        double c =in.nextDouble();
        double d = c/Math.PI; /*Если вам известна длина окружности, то,
        для того чтобы вычислить диаметр, разделите ее на Pi.
        https://goo.gl/7Pafxk */
        System.out.format("Диаметр окружности = " + "%.2f \n" ,d);

    }
}
